# calculadora_funcoes.py

# Função para somar
def add(x, y):
    return x + y

# Função para subtrair
def subtract(x, y):
    return x - y

# Função para multiplicar
def multiply(x, y):
    return x * y

# Função para dividir
def divide(x, y):
    if y != 0:
        return x / y
    else:
        return "Erro! Divisão por zero."
