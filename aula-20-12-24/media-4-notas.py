#Objetivo: Criar um Programa que Peça as 4 Notas Bimestrais e Mostre a Média

#Instruções:

#1 - Solicitar as Notas do Usuário: Use a função input() para pedir ao usuário que insira cada uma das quatro notas bimestrais. Converta a 
# entrada do usuário de string para um número (float) para permitir cálculos.

#2 - Calcular a Média das Notas: Some as quatro notas e divida o resultado por quatro para obter a média.

#3 - Mostrar a Média Calculada para o Usuário: Use a função print() para exibir a média das notas calculada.

nota1 = float(input("Informe a 1° nota: "))
nota2 = float(input("Informe a 2° nota: "))
nota3 = float(input("Informe a 3° nota: "))
nota4 = float(input("Informe a 4° nota: "))

soma = nota1 + nota2 + nota3 + nota4

resultado = soma / 4
print(f"A média das notas é {resultado}")


